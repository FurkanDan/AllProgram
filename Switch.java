public class Switch {
    public static void main(String[] args) {
        int num = 500;
        switch(num){
            case 200:
            System.out.println("case1");
            break;
            case 300:
            System.out.println("case2");
            

            case 100:
            System.out.println("case3");
            
            default :
            System.out.println("case no is not matching");
        }
    }
    
}
